package data

import (
	"bytes"
	"compress/gzip"
	"io"
)

// mobile_csv returns raw, uncompressed file data.
func mobile_csv() []byte {
	gz, err := gzip.NewReader(bytes.NewBuffer([]byte{
		0x1f, 0x8b, 0x08, 0x00, 0x00, 0x09, 0x6e, 0x88, 0x00, 0xff, 0xf2, 0xcd,
		0x4f, 0xca, 0xcc, 0x49, 0xe5, 0xca, 0x0c, 0xc8, 0xc8, 0xcf, 0x03, 0x51,
		0x89, 0x29, 0x40, 0x22, 0x3f, 0x85, 0xcb, 0x2f, 0x3f, 0x3b, 0x33, 0xd1,
		0xd9, 0x58, 0xd7, 0xc0, 0x80, 0xcb, 0x31, 0x2f, 0xa5, 0x28, 0x3f, 0x33,
		0x85, 0x0b, 0x10, 0x00, 0x00, 0xff, 0xff, 0xaf, 0xfc, 0x5f, 0x78, 0x2b,
		0x00, 0x00, 0x00,
	}))

	if err != nil {
		panic("Decompression failed: " + err.Error())
	}

	var b bytes.Buffer
	io.Copy(&b, gz)
	gz.Close()

	return b.Bytes()
}
